    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        BdUsuarios user=new BdUsuarios();
        Usuario mod=new Usuario();
        
        String pass=new String (jPasswordField1.getPassword());
        String passCon=new String(jPasswordField2.getPassword());
        
        if(jTextField1.getText().equals("") || jTextField2.getText().equals("")|| jTextField3.getText().equals("")|| jTextField4.getText().equals("")|| jTextField5.getText().equals("")
                || jTextField6.getText().equals("")||jPasswordField1.getPassword().equals("")||jPasswordField2.getPassword().equals("")){
            JOptionPane.showMessageDialog(null, "Porfavor llene todos los campos");
        }else{
        
        if(pass.equals(passCon)){
            
            if(user.existeusuario(Integer.parseInt(jTextField1.getText()))==0){
            String nuevoPass=Hash.sha1(pass);
            mod.setId(Integer.parseInt(jTextField1.getText()));
            mod.setNombre(jTextField2.getText());
            mod.setApellido(jTextField3.getText());
            mod.setPass(nuevoPass);
            mod.setTelefono(jTextField4.getText());
            mod.setFechanac(jTextField5.getText());
            mod.setCargo(jTextField6.getText());
            
            if(user.registrar(mod)){
                JOptionPane.showMessageDialog(null, "Registro Guardado");
                limpiar();
            }else{
                JOptionPane.showMessageDialog(null, "Registro Fallido");
            }
            }else{
                JOptionPane.showMessageDialog(null, "El ID ya existe");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Las contrase単as no coinciden");
        }
        }
 
    }//GEN-LAST:event_jButton1ActionPerformed

